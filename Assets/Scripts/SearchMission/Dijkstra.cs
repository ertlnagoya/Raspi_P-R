using System;
using System;
using System.Collections.Generic;
using System.Linq;


namespace Mission
{
    public class Dijkstra : ISearch
    {
        // Destructor
        ~Dijkstra() { }

        // Default Constructor
        public Dijkstra() { }

        // Copy Constructor
        public Dijkstra(Dijkstra other) { }

        // Copy Assignment Operator
        public Dijkstra Assign(Dijkstra other)
        {
            return this;
        }

        // Move Constructor
        public Dijkstra(Dijkstra other, bool move) { }

        // Move Assignment Operator
        public Dijkstra Assign(Dijkstra other, bool move)
        {
            return this;
        }
    }
}
